/******************************************************************************
  Displays two grey scale images. On the left is an image that has come from an
  image processing pipeline, just after colour thresholding. On the right is
  the result of applying an edge detection convolution operator to the left
  image. This program performs that convolution using the four pthread.
   
  Things to note:
    - A single unsigned char stores a pixel intensity value. 0 is black, 256 is
      white.
    - The colour mode used is GL_LUMINANCE. This uses a single number to
      represent a pixel's intensity. In this case we want 256 shades of grey,
      which is best stored in eight bits, so GL_UNSIGNED_BYTE is specified as
      the pixel data type.
   
  To compile:  
    cc -o ip_coursework_028_multithread ip_coursework_028_multithread.c -lglut -lGL -lm -pthread

  To Run:
	./ip_coursework_028_multithread
   
 
******************************************************************************/
#define width 100
#define height 72

unsigned char image[], results[width * height];

typedef struct arguments {
unsigned char *input;
unsigned char *output;
  int start;
  int stride;
}
arguments_t;

void edges(unsigned char *image,unsigned char *results) {
  pthread_t thread1, thread2, thread3, thread4;

  arguments_t thread1_arguments;
  thread1_arguments.start = 0;
  thread1_arguments.stride = 4;
  thread1_arguments.input = image;
  thread1_arguments.output = results;


  arguments_t thread2_arguments;
  thread2_arguments.start = 1;
  thread2_arguments.stride = 4;
  thread2_arguments.input = image;
  thread2_arguments.output = results;

  arguments_t thread3_arguments;
  thread3_arguments.start = 2;
  thread3_arguments.stride = 4;
  thread3_arguments.input = image;
  thread3_arguments.output = results;

  arguments_t thread4_arguments;
  thread4_arguments.start = 3;
  thread4_arguments.stride = 4;
  thread4_arguments.input = image;
  thread4_arguments.output = results;

  void *detect_edges();
 
  pthread_create(&thread1, NULL, detect_edges, &thread1_arguments);
  pthread_create(&thread2, NULL, detect_edges, &thread2_arguments);
  pthread_create(&thread3, NULL, detect_edges, &thread3_arguments);
  pthread_create(&thread4, NULL, detect_edges, &thread4_arguments);

  pthread_join(thread1, NULL);
  pthread_join(thread2, NULL);
  pthread_join(thread3, NULL);
  pthread_join(thread4, NULL);
}



void *detect_edges(arguments_t *args) {
  int i;
  unsigned char *in = args->input;
  unsigned char *out =args->output;
  int n_pixels = width * height;

  for(i=args->start;i<n_pixels;i+=args->stride) {
    int x, y; // the pixel of interest
    int b, d, f, h; // the pixels adjacent to x,y used for the calculation
    int r; // the result of calculate
   
    y = i / width;
    x = i - (width * y);

    if (x == 0 || y == 0 || x == width - 1 || y == height - 1) {
      results[i] = 0;
    } else {
      b = i + width;
      d = i - 1;
      f = i + 1;
      h = i - width;

      r = (in[i] * 4) + (in[b] * -1) + (in[d] * -1) + (in[f] * -1)
          + (in[h] * -1);

      if (r > 0) { // if the result is positive this is an edge pixel
        out[i] = 255;
      } else {
        out[i] = 0;
      }
    }
  }
}

int time_difference(struct timespec *start, struct timespec *finish,
                    long long int *difference) {
  long long int ds =  finish->tv_sec - start->tv_sec;
  long long int dn =  finish->tv_nsec - start->tv_nsec;

  if(dn < 0 ) {
    ds--;
    dn += 1000000000;
  }
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}

void tidy_and_exit() {
  exit(0);
}

void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}

static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, image);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, results);
  glFlush();
}

static void key_pressed(unsigned char key, int x, int y) {
  switch(key){
    case 27: // escape
      tidy_and_exit();
      break;
    default:
      printf("\nPress escape to exit\n");
      break;
  }
}

int main(int argc, char **argv) {
  signal(SIGINT, sigint_callback);
 
  printf("image dimensions %dx%d\n", width, height);

 struct timespec start, finish;  
  long long int time_elapsed;

  clock_gettime(CLOCK_MONOTONIC, &start);
 
  edges(image, results);

clock_gettime(CLOCK_MONOTONIC, &finish);
  time_difference(&start, &finish, &time_elapsed);
  printf("Time elapsed was %lldns or %0.9lfs\n", time_elapsed,
         (time_elapsed/1.0e9));

  glutInit(&argc, argv);
  glutInitWindowSize(width * 2,height);
  glutInitDisplayMode(GLUT_SINGLE | GLUT_LUMINANCE);
     
  glutCreateWindow("6CS005 Image Progessing CourseWork of HPC");
  glutDisplayFunc(display);
  glutKeyboardFunc(key_pressed);
  glClearColor(0.0, 1.0, 0.0, 1.0);

  glutMainLoop();

  tidy_and_exit();
 
  return 0;
}

